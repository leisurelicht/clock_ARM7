void wr_auto (uint8 dat)               //自动写数据

{

  chk_busy (1);

  IO0CLR=cd;

  IO0SET=rd;

  IO0PIN=(IO0PIN&0XFFFFFF00|dat);

  IO0CLR=wr;

  IO0SET=wr;

}



void wr_comm (uint8 comm)       //写命令

{

  chk_busy (0);

  IO0SET=cd;

  IO0SET=rd;

  IO0PIN=(IO0PIN&0XFFFFFF00|comm);

  IO0CLR=wr;

  IO0SET=wr;

}

void wr_data (uint8 dat)       //写数据

{


  chk_busy (0);

  IO0CLR=cd;

  IO0SET=rd;
  
  IO0PIN=((IO0PIN&0XFFFFFF00)|dat);

  IO0CLR|=wr;

  IO0SET=wr;

}

void chk_busy (uint8 autowr)    //测状态

{  IO0SET=0xff;

  IO0SET=cd;

  IO0SET=wr;

  IO0CLR=rd;

  IO0DIR &= 0xFFFFFF00;

  if(autowr)

    {while((IO0PIN&bf3)==0)

     ;}

  else

    {while((IO0PIN&bf0)==0||(IO0PIN&bf1)==0)

     ;}

  IO0SET=rd;

  IO0DIR |= 0x000000FF;

}

